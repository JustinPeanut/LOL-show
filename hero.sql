primary key(id)
);

-- 用户的充值订单
create table lol_member_wallet_order(
id int(11) auto_increment,
-- 订单名称
order_name varchar(255),
price double(5,2),
order_num varchar(255),
aipay_num varchar(255),
-- 订单描述
order_mark varchar(255),
member_id int(11),
primary key(id)
);

-- 用户的购物车订单
create table lol_member_cart_order(
id int(11) auto_increment,
order_name varchar(255),
order_price double(5,2),
-- 是否限购
purchase_limit int(2),
order_pic varchar(255),
member_id int(11),
hero_id int(11),
primary key(id)
);

